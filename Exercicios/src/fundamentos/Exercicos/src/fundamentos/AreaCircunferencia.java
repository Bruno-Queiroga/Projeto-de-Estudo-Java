package fundamentos;

public class AreaCircunferencia {

	public static void main(String[] args) {
		int raio = 3; //declaração de variavel é iniciada com o tipo, neste caso é inteiro
		double area = 5;
		final double  pi = 3.14159;
		/*
		 * declarar casa decimais e é realizada por ponto ( . ) em java e não por vírgula (,)
		OBS:  a declaração (final + tipo ) torna a variavel uma constante valor inalterável   

		 */
		 		
		
		System.out.println(raio * area * pi);
		
	}
}
